package com.noki.zwhd.model;

public class DwBean {
	private String AGID;
	private String AGCODE;
	private String AGNAME;
	private String FAGCODE;
	private String AGRADE;
	private String GOOGLEID;

	public String getAGID() {
		return AGID;
	}

	public void setAGID(String aGID) {
		AGID = aGID;
	}

	public String getAGCODE() {
		return AGCODE;
	}

	public void setAGCODE(String aGCODE) {
		AGCODE = aGCODE;
	}

	public String getAGNAME() {
		return AGNAME;
	}

	public void setAGNAME(String aGNAME) {
		AGNAME = aGNAME;
	}

	public String getFAGCODE() {
		return FAGCODE;
	}

	public void setFAGCODE(String fAGCODE) {
		FAGCODE = fAGCODE;
	}

	public String getAGRADE() {
		return AGRADE;
	}

	public void setAGRADE(String aGRADE) {
		AGRADE = aGRADE;
	}

	public String getGOOGLEID() {
		return GOOGLEID;
	}

	public void setGOOGLEID(String gOOGLEID) {
		GOOGLEID = gOOGLEID;
	}

}
