package com.noki.baobiaohuizong.qitabean;

public class GYS {
	private String gysmc;
	private String gysbm;
	
	public GYS() {
		super();
	}

	public GYS(String gysmc, String gysbm) {
		super();
		this.gysmc = gysmc;
		this.gysbm = gysbm;
	}

	public String getGysmc() {
		return gysmc;
	}

	public void setGysmc(String gysmc) {
		this.gysmc = gysmc;
	}

	public String getGysbm() {
		return gysbm;
	}

	public void setGysbm(String gysbm) {
		this.gysbm = gysbm;
	}
	
}
