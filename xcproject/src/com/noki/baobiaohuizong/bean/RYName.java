package com.noki.baobiaohuizong.bean;

public class RYName {
	private String name;
	private String cthrnumber;
	
	public RYName() {
		super();
	}

	public RYName(String name, String cthrnumber) {
		super();
		this.name = name;
		this.cthrnumber = cthrnumber;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCthrnumber() {
		return cthrnumber;
	}

	public void setCthrnumber(String cthrnumber) {
		this.cthrnumber = cthrnumber;
	}
	
}
