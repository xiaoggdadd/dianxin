public class CountForm {
    private int cg;
    private int bcg;
    private int zg;
    public CountForm() {
    }

    public void setCg(int cg) {
        this.cg = cg;
    }

    public void setBcg(int bcg) {
        this.bcg = bcg;
    }

    public void setZg(int zg) {
        this.zg = zg;
    }

    public int getCg() {
        return cg;
    }

    public int getBcg() {
        return bcg;
    }

    public int getZg() {
        return zg;
    }
}
